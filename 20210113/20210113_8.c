/* 8. Да прочетем какво се случва със стойността на променливата x:
Каква ще бъде стойността на x, ако я отпечатаме на екрана след последната
калкулация? */
#include <stdio.h>

int main()
{
    char x = 8;
    x = x - 2;      // x = 6
    x = x + 6;      // x= 12
    x = x - 10 + 2; // x = 4

    printf("%d\n", x);

    return 0;
}